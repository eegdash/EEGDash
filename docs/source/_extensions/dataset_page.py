# 1-line stub written per dataset. Sphinx reads this file, finds the
# directive, the directive emits the actual page nodes. Replaces the
# 600+-line rendered RST that the legacy generator wrote per dataset.
_SHELL_TEMPLATE = "{notice}.. dataset-page:: {name}\n"


def _write_if_changed(path: Path, content: str) -> bool:
    existing = path.read_text(encoding="utf-8") if path.exists() else None
    if existing == content:
        return False
    path.write_text(content, encoding="utf-8")
    return True


def _cleanup_stale_dataset_pages(dataset_dir: Path, expected: set[Path]) -> None:
    """Remove autogenerated leaf pages that are no longer in the catalog.

    Only deletes files starting with our ``AUTOGEN_NOTICE`` to avoid
    nuking hand-edited pages somebody dropped into the directory.
    """
    for path in dataset_dir.glob("eegdash.dataset.DS*.rst"):
        if path in expected:
            continue
        try:
            if not path.read_text(encoding="utf-8").startswith(AUTOGEN_NOTICE):
                continue
        except OSError:
            continue
        path.unlink()


def _write_dataset_shells(app) -> None:
    """builder-inited handler: emit the dataset toctree scaffolding.

    Writes (and cleans) the index page, one source-group page per
    {OpenNeuro, NeMAR, ...}, one 1-line shell per dataset, plus the two
    static templates (EEGDashDataset, EEGChallengeDataset).

    The 1-line shells exist purely so Sphinx's existing toctree
    machinery keeps working. Page bodies are emitted by the directive
    at parse time, not pre-rendered onto disk.
    """
    dataset_dir = Path(app.srcdir) / "api" / "dataset"
    dataset_dir.mkdir(parents=True, exist_ok=True)

    dataset_names = _iter_dataset_classes()
    dataset_rows = _load_dataset_rows(dataset_names)

    # Group datasets by source ({OpenNeuro, NeMAR, Other, ...}).
    datasets_by_source: dict[str, list[str]] = defaultdict(list)
    for name in dataset_names:
        row = dataset_rows.get(name) or {}
        source = _clean_value(row.get("source")) or "Other"
        datasets_by_source[source].append(name)

    # Generate group pages (small toctrees, no per-dataset content).
    group_toctree_entries: list[str] = []
    for source, names in datasets_by_source.items():
        safe_source = "".join(c if c.isalnum() else "_" for c in source).lower()
        if not safe_source:
            safe_source = "other"
        group_filename = f"source_{safe_source}.rst"
        group_path = dataset_dir / group_filename

        group_toctree = _render_toctree_entries(sorted(names))
        source_title = source.title() if source.islower() else source

        group_content = f"""{AUTOGEN_NOTICE}
{source_title} Datasets
{"=" * (len(source_title) + 9)}

.. toctree::
   :maxdepth: 1

{group_toctree}
"""
        if _write_if_changed(group_path, group_content):
            LOGGER.info("[dataset-docs] Updated group page %s", group_filename)

        group_toctree_entries.append(group_filename)

    toctree_entries_str = "\n".join(
        f"   {entry}" for entry in sorted(group_toctree_entries)
    )

    experiment_rows = _render_experiment_rows(_load_experiment_counts(dataset_names))
    index_content = DATASET_INDEX_TEMPLATE.format(
        notice=AUTOGEN_NOTICE,
        dataset_count=len(dataset_names),
        experiment_rows=experiment_rows,
        toctree_entries=toctree_entries_str,
    )

    index_path = dataset_dir / "api_dataset.rst"
    if _write_if_changed(index_path, index_content):
        LOGGER.info("[dataset-docs] Updated %s", index_path.relative_to(app.srcdir))

    base_content = BASE_DATASET_TEMPLATE.format(notice=AUTOGEN_NOTICE)
    base_path = dataset_dir / "eegdash.dataset.EEGChallengeDataset.rst"
    if _write_if_changed(base_path, base_content):
        LOGGER.info("[dataset-docs] Updated %s", base_path.relative_to(app.srcdir))

    primary_content = PRIMARY_DATASET_TEMPLATE.format(notice=AUTOGEN_NOTICE)
    primary_path = dataset_dir / "eegdash.EEGDashDataset.rst"
    if _write_if_changed(primary_path, primary_content):
        LOGGER.info("[dataset-docs] Updated %s", primary_path.relative_to(app.srcdir))

    # Per-dataset shells: 1-line stubs that delegate to the directive.
    # The directive does the heavy lifting (700+ pages worth of
    # rendered RST is no longer written to disk).
    generated_paths: set[Path] = set()
    srcdir = Path(app.srcdir)

    for name in dataset_names:
        shell_path = dataset_dir / f"eegdash.dataset.{name}.rst"
        shell_content = _SHELL_TEMPLATE.format(notice=AUTOGEN_NOTICE, name=name)
        if _write_if_changed(shell_path, shell_content):
            rel = shell_path.relative_to(srcdir)
            LOGGER.debug("[dataset-docs] Updated shell %s", rel)
        generated_paths.add(shell_path)

    # Keep group files alive so the cleanup below doesn't nuke them.
    for source in datasets_by_source:
        safe_source = "".join(c if c.isalnum() else "_" for c in source).lower()
        if not safe_source:
            safe_source = "other"
        generated_paths.add(dataset_dir / f"source_{safe_source}.rst")

    _cleanup_stale_dataset_pages(dataset_dir, generated_paths)

    # Remove legacy pages that used the short filename convention.
    for legacy in dataset_dir.glob("DS*.rst"):
        try:
            legacy.unlink()
        except OSError:
            continue


def setup(app) -> dict:
    """Register the directive and the per-build shell-writer hook."""
    app.add_directive("dataset-page", DatasetPageDirective)
    app.connect("builder-inited", _write_dataset_shells)
    return {
        "version": "0.2",
        # The directive itself is read-safe (no shared mutable state
        # outside the module-level caches, which are populated under a
        # single GIL-protected first-access path). Write-safe because
        # each page is independent.
        "parallel_read_safe": True,
        "parallel_write_safe": True,
    }
