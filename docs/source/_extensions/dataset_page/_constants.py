r"""Shared constants for the dataset_page package.

Hoisted out of the monolithic ``dataset_page.py`` so the directive,
section formatters, and shell-writer can all reach them without
importing each other (or pulling in Sphinx for things that have no
Sphinx dependency).

The two ``DATASET_NAME_RE`` definitions in the original module were
byte-identical (``r"^[A-Za-z0-9_\-]{1,64}$"``) -- collapsed to one
canonical pattern here.
"""

from __future__ import annotations

import re
from pathlib import Path

# Resolves to ``docs/source`` since this module lives in
# ``docs/source/_extensions/dataset_page/``. The original monolith
# computed it as ``Path(__file__).resolve().parent.parent``; the
# package layout is one level deeper, so use ``parent.parent.parent``.
_DOCS_SOURCE_ROOT = Path(__file__).resolve().parent.parent.parent

# Mirrors the legacy conf.py constants -- the eegdash repo lives two
# levels above ``docs/source/`` and clones land under ``ingestions/clone``.
REPO_ROOT = _DOCS_SOURCE_ROOT.parent.parent
CLONE_ROOT = REPO_ROOT / "ingestions" / "clone"


# Pinned in conf.py (and now here) so directive-rendered pages keep the
# legacy "this page is autogenerated" header. Plain ".." comment block,
# not a real RST directive, so Sphinx renders it as nothing.
AUTOGEN_NOTICE = """..
   This documentation page is generated during the Sphinx build.
   The underlying code is manually maintained and not autogenerated.

"""

DEFAULT_METADATA_FIELDS = [
    ("subject", "Subject identifier."),
    ("session", "Session identifier."),
    ("run", "Run identifier."),
    ("task", "Task label."),
    ("age", "Participant age (if available)."),
    ("gender", "Participant gender (if available)."),
    ("sex", "Participant sex (if available)."),
]


# Conservative argument grammar -- the directive only ever takes a
# Python class name like ``DS002718`` or ``EEG2025R1``. Mirrors the
# gate used by ``dataset_explorer`` / ``dataset_figure``. Dedupes the
# two byte-identical definitions in the original module.
DATASET_NAME_RE: re.Pattern[str] = re.compile(r"^[A-Za-z0-9_\-]{1,64}$")


